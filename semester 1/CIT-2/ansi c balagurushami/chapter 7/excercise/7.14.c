{
    int i;
    float j, s;
    for (j=1.1; j >= 0; j-=0.1)
    {
        for (i=0; i<= 180; i+= 15)
        {
            s = sin(i*PI/180);
            if (fabsf(s-j)<=0.1)
            {
                printf("*");
            }
            else {
                printf(" ");
            }
        }
        printf("\n");
    }
}
